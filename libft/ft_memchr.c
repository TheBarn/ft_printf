#include <string.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*str;
	unsigned char	ch;

	str = (unsigned char *)s;
	ch = (unsigned char)c;
	while (n > 0 && *str != ch)
	{
		str++;
		n--;
	}
	if (n > 0 && *str == ch)
		return (str);
	return (NULL);
}
